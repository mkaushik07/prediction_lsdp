ortable, and environment-friendly mass transport for intra and inter-urban mobility. An agglomeration of suburban communities will also be a part of this urban growth, calling for extended urban transportation solutions. Innovations to reduce environmental impact, such as pollution and noise on the urban landscape and improvement in energy efficiency for rail-based transportation modes, will be increasingly sought. Growing urbanization will also lead to an expansion in the transport networks connecting large cities as well as the smaller cities located in between the larger ones. HSR has already proven to be a highly energy-efficient and safe form of transport compared to other modes. Expansion of HSR links and the renewal of fleets should occur within mature and new markets, and the creation of new networks is expected to open opportunities for this segment.</p>




